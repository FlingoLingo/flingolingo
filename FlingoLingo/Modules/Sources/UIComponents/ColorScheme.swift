import UIKit

public enum ColorScheme {
    
    // Кнопка.
    public static let accent = UIColor(red: 210/255, green: 67/255, blue: 102/255, alpha: 1.0)
    // Основной текст.
    public static let mainText = UIColor(red: 255/255, green: 255/255, blue: 255/255, alpha: 1.0)
    // Вспомогательный текст.
    public static let secondaryText = UIColor(red: 175/255, green: 175/255, blue: 175/255, alpha: 1.0)
    // Фон.
    public static let background = UIColor(red: 27/255, green: 26/255, blue: 26/255, alpha: 1.0)
    // Темный фон.
    public static let darkBackground = UIColor(red: 18/255, green: 18/255, blue: 18/255, alpha: 1.0)
    // Неактивный элемент.
    public static let inactive = UIColor(red: 99/255, green: 99/255, blue: 99/255, alpha: 1.0)
}
